    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed



        if (!jTextFieldUsername.getText().equals("") && !jTextFieldPassword.getText().equals("")) {

            UsuarioDao uDao = new DaoFactory().buildUsuarioDao();
            Usuario u = new Usuario();

            u.setUsername(jTextFieldUsername.getText());
            u.setPassword(jTextFieldPassword.getText());

            uDao.save(u);

            jLabel3.setText("Sua conta foi criada com sucesso.");

            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "o campo \'username\' e \'password\' precisam ser preenchidos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

