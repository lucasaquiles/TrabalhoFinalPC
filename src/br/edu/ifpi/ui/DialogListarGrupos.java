    public Grupo getGrupoByRow(int row) {

        Grupo grupo = null;

        if (row != -1) {

            try {

                grupo = (Grupo) TabelaGrupos.getValueAt(row, 0);

            } catch (Exception e) {
            }
        }

        return grupo;
    }

    private void BotaoRemoverGpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoRemoverGpActionPerformed

        int row[] = TabelaGrupos.getSelectedRows();

        System.out.println("selecionados: "+row.length);

        if (row.length != -1) {

            int c = 0;

            for (int i = 0; i <= modelo.getRowCount(); i++) {

                if (i == row[c]) {

                    Grupo g = (Grupo) TabelaGrupos.getValueAt(i, 0);

                    int conf = JOptionPane.showConfirmDialog(null, "vocÃª quer excluir este grupo? [" + g + "]");

                    if (conf == 0) {

                        gDao.remove(g.getId());
                        JOptionPane.showMessageDialog(null, "grupo removido!");
                        modelo.removeRow(i);

                        if (!(c + 1 >= row.length)) {

                            c = c + 1;
                        }
                    }
                }
            }
        }

    }//GEN-LAST:event_BotaoRemoverGpActionPerformed

