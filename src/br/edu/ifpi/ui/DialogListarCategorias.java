    private void BotaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoRemoverActionPerformed
        int rows[] = TabelaCategoria.getSelectedRows();

        if (rows.length != -1) {

            int count = 0;
            for (int i = 0; i <= model.getRowCount(); i++) {

                if (i == rows[count]) {

                    Categoria c = (Categoria) TabelaCategoria.getValueAt(i, 0);

                    int conf = JOptionPane.showConfirmDialog(null, "vocÃª realmente quer excluir essa categoria? [" + c + "]");

                    if (conf == 0) {

                        categoriaDao.remove(c.getId());
                        JOptionPane.showMessageDialog(null, "categoria removida!");
                        model.removeRow(i);

                        if(!(count + 1 >= rows.length)){
                            count = count + 1;
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_BotaoRemoverActionPerformed

    private void BotaoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoAlterarActionPerformed


        Categoria c = (Categoria) TabelaCategoria.getValueAt(TabelaCategoria.getSelectedRow(), 0);

        if (c != null) {

            DialogCategoria categ = new DialogCategoria(null, true, categoriaDao, c);
            categ.setVisible(true);
        }

    }//GEN-LAST:event_BotaoAlterarActionPerformed

