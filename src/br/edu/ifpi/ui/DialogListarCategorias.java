    public void mountTableCategorias() {

       
        Vector v = null;

        ArrayList<Categoria> categorias = categoriaDao.list();

        if (categorias.size() > 0) {

            for (Categoria c : categorias) {
                v = new Vector();
                
                v.add(c);
                model.addRow(v);
            }
        }
        
        TabelaCategoria.setModel(model);
    }

