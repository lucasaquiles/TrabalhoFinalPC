    private void BotaoImportaXMLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoImportaXMLActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(this);

        fileChooser.setCurrentDirectory(new File("/contatos/imports/"));

        List<Contato> list = null;

        try {
            XStream xstream = new XStream(new DomDriver());

            list = (List<Contato>) xstream.fromXML(new FileReader(fileChooser.getSelectedFile()));

            JOptionPane.showMessageDialog(null, "carregou o arquivo: " + fileChooser.getSelectedFile().getName() + "com " + list.size() + " registros");

            mountTableFromImportedXML(list);
        } catch (FileNotFoundException ex) {

            Logger.getLogger(DialogListContatos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BotaoImportaXMLActionPerformed

    public void mountTableFromImportedXML(List<Contato> contatos) {

        DefaultTableModel model = (DefaultTableModel) TabelaContatos.getModel();

        Vector v = null;

        for (Contato c : contatos) {
        v = new Vector();

            v.add(c);
            v.add(c.getEmail());
            v.add(c.getTelefone());

            if (c.getDataNascimento() != null) {

                v.add(c.getDataNascimento());
            } else {

                v.add(null);
            }

            v.add(c.getEndereco().getNome());
            v.add(null);
            model.addRow(v);
        }
    }

    private void BotaoExportarXMLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoExportarXMLActionPerformed

        Set<Contato> contatos = usuarioLogado.getUsuario().getContatos();

        XStream xtream = new XStream();

        ArrayList<Contato> contatosList = new ArrayList<Contato>();

        for (Contato c : contatos) {

            contatosList.add(c);
        }

        String f = xtream.toXML(contatosList);

        try {

            BufferedWriter wr = new BufferedWriter(new FileWriter("contatos_exports/exported/contatos_" + usuarioLogado.getUsuario().getUsername() + ".xml"));

            wr.append(f);
            wr.close();

            JOptionPane.showMessageDialog(null, "Os dados foram exportados");
        } catch (IOException ex) {

            Logger.getLogger(DialogListContatos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BotaoExportarXMLActionPerformed

    private void BotaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoRemoverActionPerformed

        int row[] = TabelaContatos.getSelectedRows();

        if (row.length != -1) {

            DefaultTableModel model = (DefaultTableModel) TabelaContatos.getModel();
            int c = 0;
            for (int i = 0; i <= model.getRowCount(); i++) {


                if (i == row[c]) {

                    Contato con = (Contato) TabelaContatos.getValueAt(i, 0);

                    usuarioLogado.getUsuario().getContatos().remove(con);
                    usuarioLogado.getUsuario().setContatos(usuarioLogado.getUsuario().getContatos());

                    int conf = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o Contato: " + c + "?");

                    if (conf == 0) {

                        new DaoFactory().buildUsuarioDao().update(usuarioLogado.getUsuario());

                        model.removeRow(i);

                        if (!(c + 1 >= row.length)) {

                            c = c + 1;
                        }
                    }
                }
            }
            JOptionPane.showMessageDialog(null, "contatos removidos");
        }
    }//GEN-LAST:event_BotaoRemoverActionPerformed

